CREATE TABLE BRANDS (
    BRAND_CODE int generated by default as identity primary key,
    BRAND_NAME VARCHAR(50) NOT NULL
);

CREATE TABLE CATEGORY (
    CATEGORY_CODE int generated by default as identity primary key,
    CATEGORY_NAME VARCHAR(50) NOT NULL
);

DROP TABLE IF EXISTS ITEMS;
CREATE TABLE ITEMS (
    ITEM_NO bigint generated by default as identity primary key,
    BRAND_CODE int NOT NULL,
    CATEGORY_CODE int NOT NULL,
    PRICE DECIMAL NOT NULL,
    FOREIGN KEY (BRAND_CODE) REFERENCES BRANDS(BRAND_CODE),
    FOREIGN KEY (CATEGORY_CODE) REFERENCES CATEGORY(CATEGORY_CODE)
);

